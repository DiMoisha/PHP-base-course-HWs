<h2>Эмульсии и мастики</h2>
<p><span style="line-height: normal;">Имеющееся отечественное и импортное оборудование позволяет производить все известные виды анионных и катионных эмульсий; от простейших эмульсий до латексных эмульсий, используемых для устройства тонкослойных покрытий.&nbsp;</span></p>
<p>&nbsp;</p>
<p><span style="line-height: normal;">По заявке заказчика эмульсия доставляется непосредственно на обьект.&nbsp;</span></p>
<p>&nbsp;</p>
<p><span style="line-height: normal;">Битумные мастики применяются для заделки трещин в покрытии и для кровельных работ. Поставляются в упакованном виде.</span></p>
